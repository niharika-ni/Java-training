public class Sum {
    public static void main(String[] args) {
        // Given numbers
        int num1 = 5;
        int num2 = 10;

        // Calculate the sum of the two numbers
        int sum = num1 + num2;

        // Print the result
        System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}