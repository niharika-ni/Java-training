public class Division {
    public static void main(String[] args) {
        // Given numbers
        double num1 = 10;
        double num2 = 5;

        // Calculate the division
        double divisionResult = num1 / num2;

        // Print the result
        System.out.println("Division of " + num1 + " and " + num2 + " is: " + divisionResult);
    }
}
